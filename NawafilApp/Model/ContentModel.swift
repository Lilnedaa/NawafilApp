import SwiftUI
import Foundation


struct CardData: Identifiable {
    let id = UUID()
    let title: String
    let content: String
}

enum NawafilTopic: String, CaseIterable, Identifiable {
    // Prayer topics
    case duha = "صلاة الضحى"
    case qiyam = "قيام الليل"
    case sunnahFajr = "سنة الفجر"
    case sunnahDhuhr = "سنة الظهر"
    case sunnahMaghrib = "سنة المغرب"
    case sunnahIsha = "سنة العشاء"
    
    // Fasting topics
    case mondayThursday = "صيام الاثنين والخميس"
    case whiteDays = "صيام الأيام البيض"
    case arafah = "صيام يوم عرفة"
    case ashura = "صيام يوم عاشوراء"
    case shawwal = "صيام ستة من شوال"

    var id: String { rawValue }

    var cards: [CardData] {
        switch self {
            
            // PRAYER TOPICS
        case .duha:
            return [
                CardData(
                    title: "حكمها:",
                    content: """
        صلاة الضحى سنة مؤكدة، أوصى بها النبي ﷺ بعض أصحابه، وفعلها في بعض الأحيان، وصلاها يوم الفتح ثمان ركعات.
        """
                ),
                CardData(
                    title: "وقتها:",
                    content: """
        من ارتفاع الشمس قيد رمح إلى قبيل الظهر.
        تصح في أول الوقت أو وسطه، وأفضلها عند اشتداد الضحى حين يشتد الحر.
        """
                ),
                CardData(
                    title: "عدد ركعاتها:",
                    content: """
        أقلها ركعتان بتسليمة واحدة.
        ويجوز أربعًا، أو ستًا، أو ثمانًا، أو أكثر، ويسلم من كل ثنتين.
        """
                )
            ]
            
        case .qiyam:
            return [
                CardData(
                    title: "حكمها:",
                    content: """
        قيام الليل مشروع وسنة مؤكدة، ومن أعمال الصالحين، ومن فعل النبي ﷺ.
        """
                ),
                CardData(
                    title: "وقتها:",
                    content: """
        في أول الليل، أو وسطه، أو آخره، وآخره أفضل، والثلث الأخير أفضل، إلا إن شق عليه فيوتر أول الليل.
        """
                ),
                CardData(
                    title: "كيفيتها:",
                    content: """
        يصلي ركعتين ركعتين، ويسلم من كل ثنتين، ويوتر بواحدة.
        يوتر بواحدة، أو بثلاث، أو بخمس، أو بسبع، أو أكثر، والأمر واسع.
        وأفضلها إحدى عشرة أو ثلاث عشرة ركعة تأسيًا بالنبي ﷺ.
        """
                ),
                CardData(
                    title: "القراءة فيها:",
                    content: """
        يقرأ ما تيسر من أول القرآن أو أوسطه أو آخره، أو ينظم ختمه.
        والسنة الترتيل، ويستحب السؤال عند آية الرحمة، والتعوذ عند آية الوعيد، والتسبيح عند آية التسبيح.
        """
                ),
                CardData(
                    title: "صفتها:",
                    content: """
        السنة الطمأنينة وعدم العجلة في الركوع والسجود والقراءة، والخشوع فيها تأسيًا بالنبي ﷺ.
        """
                )
            ]
            
        case .sunnahFajr:
            return [
                CardData(
                    title: "حكمها",
                    content: """
سنة الفجر ركعتان قبل صلاة الفجر،
وهي من أكثر السنن تأكيداً ولم يكن النبي ﷺ يتركها.
"""
                ),
                CardData(
                    title: "فضلها",
                    content: """
قال النبي ﷺ: «ركعتا الفجر خير من الدنيا وما فيها».
"""
                ),
                CardData(
                    title: "وقتها",
                    content: """
تصلى بعد دخول وقت الفجر وقبل صلاة الفجر المفروضة.
"""
                )
            ]
            
        case .sunnahDhuhr:
            return [
                CardData(
                    title: "حكمها",
                    content: """
        سنة مؤكدة ومستحبة، ينبغي المحافظة عليها وعدم تركها.  
        وثبت عن عائشة رضي الله عنها عن النبي ﷺ: أنه كان لا يدع أربعاً قبل الظهر (رواه البخاري في الصحيح)
        """
                ),
                CardData(
                    title: "وقتها",
                    content: """
        قبل الظهر أربع ركعات، وبعد الظهر ركعتان.  
        فالأفضل أن يصلي المؤمن والمؤمنة أربعاً قبل الظهر وثنتين بعدها
        """
                ),
                CardData(
                    title: "كيفيتها",
                    content: """
        تصلى أربع ركعات قبل الظهر ثم ركعتان بعدها.  
        وإن اكتفى بثنتين قبل وبعد الظهر فلا بأس، ولكن الأفضل اتباع الحديثين جميعاً
        """
                ),
                CardData(
                    title: "فضلها",
                    content: """
        من صلى أربعاً قبل الظهر وأربعاً بعدها حرمه الله على النار، وهذا فضل عظيم.  
        عن أم حبيبة رضي الله عنها، عن النبي ﷺ: «من صلى أربعاً قبل الظهر وأربعاً بعدها حرمه الله على النار»
        """
                )
            ]

        case .sunnahMaghrib:
            return [
                CardData(
                    title: "السنة بعد المغرب:",
                    content: """
        المعروف عنه ﷺ أنه كان يصلي بعد المغرب ركعتين راتبة، وهي ثابتة عنه في الصحيحين وغيرهما.
        """
                ),
                CardData(
                    title: "السنة قبل المغرب:",
                    content: """
        رُوي عنه ﷺ أنه صلى قبل المغرب ركعتين، لكن فعله محل نظر، وإن صححه ابن حبان.
        """
                ),
                CardData(
                    title: "دليل مشروعيتها:",
                    content: """
        قال ﷺ: «صلوا قبل المغرب» ثم قال في الثالثة: «لمن شاء».
        وكان الصحابة يصلون ركعتين وهو ينظر إليهم ويقرهم على ذلك، فثبتت من قوله وتقريره.
        """
                )
            ]
            
        case .sunnahIsha:
            return [
                CardData(
                    title: "حكمها",
                    content: """
        راتبة العشاء ركعتان، هكذا كان النبي ﷺ يفعل، والقيام بعد العشاء يجوز أكثر من ذلك، الليل كله محل صلاة، والأمر واسع
        """
                ),
                CardData(
                    title: "وقتها",
                    content: """
        يصلي ما شاء في أول الليل، أو وسطه، أو آخره، ثم يختم بواحدة وهي الوتر
        """
                ),
                CardData(
                    title: "كيفيتها وعدد ركعاتها",
                    content: """
        الراتبة بعد العشاء ركعتان، قيام
"""
                )
        ]
            
        // FASTING TOPICS
        case .mondayThursday:
            return [
                CardData(
                    title: "حكمه",
                    content: """
صيام يومي الاثنين والخميس من كل أسبوع سنة مستحبة.
كان النبي ﷺ يتحرى صيامهما.
"""
                ),
                CardData(
                    title: "فضله",
                    content: """
قال النبي ﷺ: «تُعرض الأعمال يوم الاثنين والخميس
فأحب أن يُعرض عملي وأنا صائم».
"""
                ),
                CardData(
                    title: "سبب الفضل",
                    content: """
تُرفع الأعمال إلى الله في هذين اليومين،
فيحب المسلم أن يُرفع عمله وهو صائم.
"""
                )
            ]
            
        case .whiteDays:
            return [
                CardData(
                    title: "حكمه",
                    content: """
صيام الأيام البيض هو صيام الأيام ١٣ و١٤ و١٥ من كل شهر قمري.
وهو سنة مستحبة.
"""
                ),
                CardData(
                    title: "فضله",
                    content: """
قال النبي ﷺ: «صيام ثلاثة أيام من كل شهر
صيام الدهر كله».
"""
                ),
                CardData(
                    title: "سبب التسمية",
                    content: """
سميت بالأيام البيض لأن القمر يكون بدراً فيها
فتكون الليالي بيضاء مضيئة.
"""
                )
            ]
            
        case .arafah:
            return [
                CardData(
                    title: "حكمه",
                    content: """
صيام يوم عرفة سنة مؤكدة لغير الحاج.
أما الحاج فالأفضل له الفطر.
"""
                ),
                CardData(
                    title: "فضله",
                    content: """
قال النبي ﷺ: «صيام يوم عرفة أحتسب على الله
أن يكفر السنة التي قبله والسنة التي بعده».
"""
                ),
                CardData(
                    title: "وقته",
                    content: """
يوم عرفة هو اليوم التاسع من شهر ذي الحجة.
وهو من أفضل أيام السنة.
"""
                )
            ]
            
        case .ashura:
            return [
                CardData(
                    title: "حكمه",
                    content: """
صيام يوم عاشوراء سنة مؤكدة.
ويُستحب صيام يوم قبله أو بعده.
"""
                ),
                CardData(
                    title: "فضله",
                    content: """
قال النبي ﷺ: «صيام يوم عاشوراء أحتسب على الله
أن يكفر السنة التي قبله».
"""
                ),
                CardData(
                    title: "وقته",
                    content: """
يوم عاشوراء هو اليوم العاشر من شهر محرم.
والأفضل صيامه مع التاسع أو الحادي عشر.
"""
                )
            ]
            
        case .shawwal:
            return [
                CardData(
                    title: "حكمه",
                    content: """
صيام ستة أيام من شوال بعد عيد الفطر سنة مستحبة.
يمكن صيامها متتابعة أو متفرقة.
"""
                ),
                CardData(
                    title: "فضله",
                    content: """
قال النبي ﷺ: «من صام رمضان ثم أتبعه ستاً من شوال
كان كصيام الدهر».
"""
                ),
                CardData(
                    title: "كيفيته",
                    content: """
يُصام بعد عيد الفطر في أي وقت من شهر شوال،
سواء متتابعة أو متفرقة.
"""
                )
            ]
        }
    }
}
